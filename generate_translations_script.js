// Simple translation function
function translateToGerman(text) {
	// Direct word replacements
	let result = text;

	// Replace common phrases
	for (const [en, de] of Object.entries(translations)) {
		const regex = new RegExp('\\b' + en + '\\b', 'gi');
		result = result.replace(regex, de);
	}

	// If no translation found, return original with note
	if (result === text) {
		return text; // Keep English if no translation
	}

	return result;
}

